#!/usr/bin/env python3
"""
Generate a template potential.in file from training.in and L0 configs.

This script:
1. Reads training.in to get distance_map and other settings
2. Reads all configs from data_al/L0/
3. Detects all interaction types (bonds, angles, dihedrals, special_types)
4. Creates a template potential.in with:
   - MorseBond for bonds
   - LJ for pair-wise (PW)
   - harmonic for angles
   - Fourier for dihedrals
   All with 1.0 regularization and physically realistic bounds.
"""

